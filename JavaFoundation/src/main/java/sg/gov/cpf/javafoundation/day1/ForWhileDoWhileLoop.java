package sg.gov.cpf.javafoundation.day1;

public class ForWhileDoWhileLoop {

	public static void main(String[] args) {

		for (int i=0;i<5;i++) {
			System.out.println(i);
		}
		int x=0;
		while (x<5){
			System.out.println(x);
			x++;
		}
		
		int y=0;
		do {
			System.out.println(y);
			y++;
		}while(y<5);
		

	}

}
